        <h2>The Background</h2>
        <p>Nigeria's National Information and Technology Development Agency (NITDA) issued the 2019 Nigeria Data Protection Regulation (NDPR) to safeguard the rights of natural persons to the privacy of their Personal Data by, among other measures, regulating transactions involving the collection, use and exchange of Personal Data.</p>
        <p>The NDP springs from the shadows of the European Union's Global Data Protection Regulation 2018 (GDPR), a product of over 50 years of privacy law development in Europe. NITDA seeing the dearth of data protection practitioners in Nigeria, instituted the ingenious idea of appointing Data Protection Compliance Organisations (DPCOs) with the view of the DPCOs providing NDP and related services (training, auditing and consulting) and products. </p>
        <p> 21 Search Ltd (a recruitment, trainings and certifications company) and Taxaide Technologies Ltd (a DPCO and technology development company) have established the NDP Academy® (www.NDPacademy.ng) with the aim of providing NDP training and NITDA-approved certiﬁcation. It is currently estimated that there are over 1.5million organisations in Nigeria that meet the NITDA ﬁling/compliance threshold of 1,000 Data Subjects; which fact suggests that each of those organisations will require, at least one personnel, sufﬁciently versed in NDP and Nigerian privacy rights law in general. This is the essence of the NDP Academy®, to train and certify a generation of persons that can foster the cause of privacy rights and the NDP (including its future form), speciﬁcally.  </p>
